  bool write(uint8_t *buffer, size_t size)
  {
    calCRC(buffer, size);
    if(!isopened()){
      std::cerr << "serial not opened" << std::endl;
      return false;
    }
    size_t len = _ser.write(buffer, size + 2);
    if (len < size)
    {
      std::cerr << "write buffer length error" << std::endl;
      return false;
    }
    return true;
  }

  bool read(uint8_t *buffer, size_t size)
  {
    if(!isopened()){
      std::cerr << "serial not opened" << std::endl;
      return false;
    }
    _ser.waitReadable();
    _ser.waitByteTimes(size);
    size_t len = _ser.read(buffer, size);
    if (len < size)
    {
      std::cerr << "read buffer length error" << std::endl;
      return false;
    }
    else if (!verifyCRC(buffer, size))
    {
        std::cerr << ("read buffer verify CRC failed") << std::endl;
        return false;
    }
    return true;
  }

