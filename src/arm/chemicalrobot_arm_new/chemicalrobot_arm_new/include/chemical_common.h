  {
    serial::Timeout timeout = serial::Timeout::simpleTimeout(100);
    _ser.setPort(dev);
    _ser.setBaudrate(baudrate);
    _ser.setTimeout(timeout);
  }
  
  bool connect(){
    _ser.close();
    if (!_ser.isOpen())
    {
      try{
        _ser.open();
      }
      catch(serial::IOException e){
        std::cerr << e.what() << std::endl;
        return false;
      }
    }
    return true;
  }

  void disconnect(){
    _ser.flush();
    _ser.close();
  }

